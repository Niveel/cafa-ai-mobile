function applyParams(template: string, params?: Record<string, string>) {
  if (!params) return template;
  return Object.entries(params).reduce((acc, [key, value]) => acc.replace(new RegExp(`{{${key}}}`, 'g'), value), template);
}

export function translate(language: AppLanguage, key: string, params?: Record<string, string>) {
  const value = translations[language][key] ?? translations.en[key] ?? key;
  return applyParams(value, params);
}

export function getLanguageLabel(language: AppLanguage) {
  return LANG_LABELS[language];
}

